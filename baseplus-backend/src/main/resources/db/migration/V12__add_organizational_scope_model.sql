where not exists (select 1 from permissions where name = 'ORGANIZATION_UNITS_CREATE');

insert into permissions (name, description)
select 'ORGANIZATION_UNITS_EDIT', 'Editar estrutura organizacional.'
where not exists (select 1 from permissions where name = 'ORGANIZATION_UNITS_EDIT');

insert into permissions (name, description)
select 'ORGANIZATION_UNITS_DELETE', 'Excluir estrutura organizacional.'
where not exists (select 1 from permissions where name = 'ORGANIZATION_UNITS_DELETE');

insert into permissions (name, description)
select 'ROLES_MANAGE_ORGANIZATION_SCOPES', 'Gerenciar escopos organizacionais de perfis.'
where not exists (select 1 from permissions where name = 'ROLES_MANAGE_ORGANIZATION_SCOPES');

insert into role_permissions (role_id, permission_id)
select r.id, p.id
from roles r
join permissions p on p.name in (
    'ORGANIZATION_UNITS_VIEW',
    'ORGANIZATION_UNITS_CREATE',
    'ORGANIZATION_UNITS_EDIT',
    'ORGANIZATION_UNITS_DELETE',
    'ROLES_MANAGE_ORGANIZATION_SCOPES'
)
where upper(r.name) = 'ADMIN'
  and not exists (
      select 1
      from role_permissions rp
      where rp.role_id = r.id
        and rp.permission_id = p.id
  );
